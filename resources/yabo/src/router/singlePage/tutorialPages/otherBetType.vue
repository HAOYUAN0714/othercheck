      </ul>
      <span class="subTitle">2.开球队伍</span>
      <ul>
        <li>开球队伍是指投注于赛事中先开球的队伍。</li>
        <li>如果开球后赛事中止,所有投注仍然有效。</li>
      </ul>
      <span class="subTitle">3.主队总进球数与客队总进球数</span>
      <ul>
