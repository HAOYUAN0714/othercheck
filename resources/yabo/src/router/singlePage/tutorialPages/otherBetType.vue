        <li>主队表示首先被提及的队伍, 客队表示第二个被提及的队伍。</li>
        <li>如果总进球数大于「大小盘」所指定盘口, 则投注「大于」为赢；如果总进球数低于「大小盘」所指定盘口, 则投注「小于」为赢。</li>
        <li>如果其中一场（或更多）的赛事延期或中止, 则投注作废。</li>
      </ul>
      <span class="subTitle">4.球队大小盘</span>
      <ul>
        <li>球队大小盘是指投注中一支球队在比赛中的得分。</li>
        <li>如果总进球数大于「大小盘」所指定盘口, 则投注「大于」为赢；如果总进球数低于「大小盘」所指定盘口, 则投注「小于」为赢。</li>
      </ul>
