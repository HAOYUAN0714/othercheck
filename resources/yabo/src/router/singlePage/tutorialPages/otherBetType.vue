      <span class="subTitle">10.点球</span>
      <ul>
        <li>点球是投注于赛事中有被判罚并开出的点球。</li>
        <li>如果在第一次判罚点球后赛事中止, 所有投注仍有效。</li>
        <li>如果在第一次判罚点球前赛事中止, 所有投注均作废。</li>
      </ul>
      <span class="subTitle">11.点球大战</span>
      <ul>
        <li>是投注于点球大战中获胜的球队。</li>
        <li>在让分盘中, 投注结果以点球大战中所有得分的点球数作结算, 包括突然死亡. 在大小盘中, 投注结果仅以点球大战中正规十颗点球数作结算, 不包括突然死亡.</li>
        <li>若一方球队的成功进球数超出对方球队潜在进球可能性的次数，点球大战将立刻停止即使剩余的点球还没进行。</li>
      </ul>
      <span class="subTitle">12.梦幻对垒</span>
      <ul>
        <li>梦幻对垒是将不同赛事中的队伍配对比赛。</li>
        <li>在配对的赛事中, 两支配对的球队必须在同一天开球比赛, 否则投注均作废。</li>
        <li>所有场地仅限于参考之用。</li>
      </ul>
