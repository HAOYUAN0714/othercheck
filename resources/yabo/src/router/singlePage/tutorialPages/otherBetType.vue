          </ul>
          <ul>
            <span class="thirdTitle">第60分钟的大小盘</span><br>
            45:01 – 60:00 OU: 投注于45:01 – 60:00分的总获分大于或小于所指定的盘口, 必须在第60分钟或之前进行投注。
          </ul>
          <ul>
            <span class="thirdTitle">第75分钟的大小盘</span><br>
            60:01 – 75:00 OU: 投注于60:01 – 75:00分的总获分大于或小于所指定的盘口, 必须在第75分钟或之前进行投注。
          </ul>
          <ul>
            <span class="thirdTitle">第90分钟的大小盘</span><br>
