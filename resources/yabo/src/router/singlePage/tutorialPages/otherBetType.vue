        <li>对于30:01-45:00和75:01 - 90:00两段时间, 投注将于现场进球准确时间 (球已超过球门线), 现场电子记分牌公布的角球数 (已开出的角球) 和罚牌数 (由官方裁判员发出) 立即进行结算, 结算结果并不包括任何加时或伤停补时。</li>
